package entities;

public class Tabuada {
	
	public static int soma(int x, int y) {
		
		int resultado = x + y;

		return resultado;
	}

	public static int subtracao(int x, int y) {

		int resultado = x - y;

		return resultado;
	}

	public static int multiplicacao(int x, int y) {

		int resultado = x * y;

		return resultado;
	}

	public static int divisao(int x, int y) {

		int resultado = x / y;

		return resultado;
	}
}
